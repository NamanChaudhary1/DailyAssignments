def is_prime(num):
    if num < 2:
        return False
    for i in range(2, int(num ** 0.5) + 1):
        if num % i == 0:
            return False
    return True

L = [11, 12, 13, 14]
prime_sum = sum(num for num in L if is_prime(num))
print("Sum of all prime numbers in L:", prime_sum)
